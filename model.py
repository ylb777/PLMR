class PLMR(nn.Module):
    def __init__(self, args):
        super(PLMR, self).__init__()
        self.num_labels = args.num_labels
        self.args = args
        self.bert = BertModel.from_pretrained("/newdisk/ylb/Transformers/bert-base-uncased", return_dict=True)

        self.dropout = nn.Dropout(0.2)
        self.classifier = nn.Linear(768, args.num_labels)

        self.cls_predictor = nn.Sequential(self.dropout,
                                           self.classifier)

    def forward(
            self,
            input_ids=None,
            attention_mask=None,
            token_type_ids=None,
            position_ids=None,
            head_mask=None,
            inputs_embeds=None,
            labels=None,
            output_attentions=None,
            output_hidden_states=True,
            return_dict=True,
            mode_train=None,
    ):
        return_dict = return_dict if return_dict is not None else self.config.use_return_dict
        outputs = self.bert(
            input_ids,
            attention_mask=attention_mask,
            token_type_ids=token_type_ids,
            position_ids=position_ids,
            head_mask=head_mask,
            inputs_embeds=inputs_embeds,
            output_attentions=output_attentions,
            output_hidden_states=output_hidden_states,
            return_dict=return_dict,
            mode_train=mode_train,
            args=self.args,
        )
        my_hidden_states = outputs.hidden_states[12]
        last_cls = my_hidden_states[:, 0]
        logits = self.cls_predictor(last_cls)

        dim_reduction_start = self.args.dim_reduction_start
        dim_reduction_end = self.args.dim_reduction_end
        level = []
        for i in range(dim_reduction_end - dim_reduction_start):#计算每一层的百分比
            mid = math.exp(self.args.sparsity_percentage)*(i+1) + math.exp(1)*(dim_reduction_end - dim_reduction_start - i - 1)
            mid = mid/(dim_reduction_end - dim_reduction_start)
            level.append(math.log(mid))

        loss = None
        dim_reduction_loss_list = []
        continuity_loss_list = []

        if labels is not None:
            loss_fct = CrossEntropyLoss()
            loss = loss_fct(logits.view(-1, self.num_labels), labels.view(-1))

        layer_neat_mac = list()

        j = 0
        for i in range(dim_reduction_start+1, dim_reduction_end+1):
            mask = outputs.dim_reduction_mask[i]
            clone_mask = mask[:,1:]
            mask = clone_mask
            sparsity = torch.sum(mask) / torch.sum(attention_mask)
            dim_reduction_loss =torch.abs(sparsity - level[j])
            continuity_loss = torch.mean(torch.abs(mask[:, 1:] - mask[:, :-1]))
            j += 1
            dim_reduction_loss_list.append(dim_reduction_loss)
            continuity_loss_list.append(continuity_loss)
        classification_loss = self.args.cls_lambda * loss

        total_dim_reduction_loss1 = self.args.sparsity_lambda * dim_reduction_loss_list[0]
        total_dim_reduction_loss2 = self.args.continuity_lambda * continuity_loss_list[0]
        for i in range(dim_reduction_end - dim_reduction_start - 1):
            total_dim_reduction_loss1 = total_dim_reduction_loss1 + self.args.sparsity_lambda * dim_reduction_loss_list[i + 1]
            total_dim_reduction_loss2 = total_dim_reduction_loss2 + self.args.continuity_lambda * continuity_loss_list[i + 1]
        total_dim_reduction_loss = total_dim_reduction_loss1 + total_dim_reduction_loss2
        loss = total_dim_reduction_loss + classification_loss
        return my_Output3(
            loss=loss,
            logits=logits,
            hidden_states=outputs.hidden_states,
            attentions=outputs.attentions,
            attention_mask=outputs.attention_mask,
            dim_reduction_mask=outputs.dim_reduction_mask,
            dim_reduction_loss=total_dim_reduction_loss,
            classification_loss=classification_loss,
            sparsity_loss=total_dim_reduction_loss1,
            continuity_loss=total_dim_reduction_loss2,
            layer_tokens_remained=layer_neat_mac,
        )
